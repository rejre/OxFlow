SUPPORTED_LANGUAGES = list(TRANSLATIONS.keys())
DEFAULT_VERSION = "v2.0"


class I18nManager:
    def __init__(self, lang: str = "en"):
        self.version = DEFAULT_VERSION
        self._lang = None
        self._strings: dict = {}
        self.set_language(lang)

    def set_language(self, lang: str):
        if lang not in TRANSLATIONS: lang = "en"
        self._lang = lang
        self._strings = TRANSLATIONS[lang]
        self.version = self._strings.get("version", DEFAULT_VERSION)

    @property
    def current_language(self) -> str: return self._lang

    def get(self, key: str, default: str = "") -> str:
        value = self._strings.get(key)
        if value is None: value = TRANSLATIONS["en"].get(key, default or key)
        return value

    def __getitem__(self, key: str) -> str: return self.get(key)
